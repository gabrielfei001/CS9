from lab04 import solveMaze
from Stack import Stack
def test_solveMaze():
    maze = [
    ['+','+','+','+','+','+'],
    ['+',' ','+',' ',' ','G'],
    ['+',' ',' ',' ','+','+'],
    ['+',' ','+','+',' ','+'],
    ['+',' ',' ',' ',' ','+'],
    ['+','+','+','+','+','+'] ]
    assert solveMaze(maze, 4, 4) == True
    assert maze == [
    ['+', '+', '+', '+', '+', '+'],
    ['+', 8, '+', 11, 12, 'G'],
    ['+', 7, 9, 10, '+', '+'],
    ['+', 6, '+', '+', 2, '+'],
    ['+', 5, 4, 3, 1, '+'],
    ['+', '+', '+', '+', '+', '+'] ]
    maze = [
    ['+','+','+','+','+','+'],
    ['+',' ','+',' ',' ','G'],
    ['+',' ',' ',' ','+','+'],
    ['+',' ','+','+',' ','+'],
    ['+',' ',' ',' ',' ','+'],
    ['+','+','+','+','+','+'] ]
    assert solveMaze(maze, 1, 4) == True
    assert maze == [
    ['+','+','+','+','+','+'],
    ['+',' ','+',' ', 1,'G'],
    ['+',' ',' ',' ','+','+'],
    ['+',' ','+','+',' ','+'],
    ['+',' ',' ',' ',' ','+'],
    ['+','+','+','+','+','+'] ]
    maze = [
    ['+','+','+','+','+','+'],
    ['+',' ','+',' ',' ','+'],
    ['+',' ',' ',' ','+','+'],
    ['+',' ','+','+','G','+'],
    ['+',' ',' ',' ',' ','+'],
    ['+','+','+','+','+','+'] ]
    assert solveMaze(maze, 1, 4) == True
    assert maze == [
    ['+','+','+','+','+','+'],
    ['+', 6,'+', 2, 1,'+'],
    ['+', 5, 4, 3,'+','+'],
    ['+', 7,'+','+','G','+'],
    ['+', 8, 9, 10, 11,'+'],
    ['+','+','+','+','+','+'] ]

